package com.sims.springbootsims.entity;

/**
 * 数据可视化 Bean
 */
public class EchartsBean {

    private String name;

    private int value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
